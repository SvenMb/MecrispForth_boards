: show-logo ( -- )  \ show the JeeLabs logo
  clear
  logo  OLED.LARGE 1+ 32 * 0 do
    64 0 do
      dup i 5 rshift cells + @
      i not $1F and rshift
      1 and if i 32 + j putpixel then
    loop
    8 +
  loop
  drop display ;

